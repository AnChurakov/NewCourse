                                <p class="text normal">
                                    Сварщик – специалист по металлу, который соединяет металлические детали в сложные конструкции при помощи различных видов сварки.
                                    <br>От профессионального сварщика зависит качество работы конструкции. В его работе не допускаются ошибки, которые могут привести к катастрофическим последствиям.  Профессия сварщика подразделяется на несколько специальностей: газосварщик, сварщик ручной дуговой сварки, сварщик частично механизированной сварки (наплавки).
